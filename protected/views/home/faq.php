            <p class="question-title">Вредно ли делать КТ/МРТ?</p>
            <div class="answer">
                <div class="line"></div>
                КТ связана с определенной, хоть и небольшой лучевой нагрузкой на организм. Что так или иначе создает риск развития онкологических процессов. По этой причине делать КТ нужно строго по направлению врача и при наличии показаний.
                <br/>
                Огромным плюсом МРТ является отсутствие ионизирующего излучения, потому данная процедура совершенно безопасна. В медицине отсутствуют исследования, которые бы подтвердили какой-либо вред здоровью, нанесенный МРТ.
