            <p class="question-title">Какая требуется подготовка к МРТ/КТ?</p>
            <div class="answer">
                <div class="line"></div>
                Подготовка к КТ требуется при проведении обследования кишечника, органов брюшной полости и малого таза. Проводится она за день до исследования: КТ делается натощак, с предварительной чисткой кишечника и биохимическим анализом крови. В остальных случаях подготовка не требуется.
                <br/>
                Подготовка к МРТ зависит от обследуемого органа и может достаточно сильно различаться. В случае, если врач не обозначил каких-либо указаний по подготовке, вполне допустимо придерживаться обычного распорядка дня. Для МРТ головного мозга, позвоночника и суставов подготовка не нужна.
