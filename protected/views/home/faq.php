            <p class="question-title">Каковы противопоказания к МРТ/КТ?</p>
            <div class="answer">
                <div class="line"></div>
                МРТ противопоказана пациентам, которые имеют содержащие металл имплантаты, кардиостимулятор, спирали, несъемные коронки и тому подобное. Это связано с тем, что аппарат МРТ может нарушить действие стимулятора или либо же спровоцировать движение металлического предмета, что чревато травмами, также возможны помехи в показаниях прибора. Относительным противопоказанием является клаустрофобия: пациенту может стать плохо в томографе.
                <br/>
                Противопоказанием к проведению КТ является беременность, в особенности в первом триместре – лучевая нагрузка может привести к развитию патологий плода. Из-за этой же нагрузки стараются не проводить обследования детям младшего школьного возраста. Также к противопоказаниям относится почечная недостаточность, металлическая конструкция в исследуемой области, могут быть противопоказаниями клаустрофобия и психические расстройства. Тем не менее, абсолютных противопоказаний у КТ нет: в перечисленных случаях присутствует лишь некоторый риск определенных последствий.
                <br/>
                При использовании контраста может быть противопоказанием аллергическая реакция на йодосодержащий препарат.
